#calcular el area de un circulo
print ("Calcular el area del circulo")
pi=3.14
radio=float(input("Introduzca el radio en cm \n"))
area=pi*radio**2
print("El area del circulo es:", area)

#calcular el area de un triangulo
print ("Calcular el area del triangulo")
base=float(input("Introduzca la base en cm: \n "))
altura=float(input("Introduzca la altura en cm: \n "))
area= (base) * int (altura) / 2.0
print ("el area del triangulo es: ", area)

#calcular el area de un cuadrado
print ("Calcular el area del cuadrado")
lado = input("Ingresa el lado en cm: \n")
lado = float(lado)
