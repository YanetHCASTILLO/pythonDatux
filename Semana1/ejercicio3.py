#Operación suma
print ("Calcular la suma de dos numeros")
a=float(input("Introduzca el primer numero \n"))
b=float(input("Introduzca el segundo numero \n"))
c=float(input("Introduzca el tercer numero \n"))
suma = a+b+c
resta = abs(a+b-c)
multi = a*b*c
div = (a*b)/c
DivEnt= (a*b)//c

print ("La suma es: ", suma)
print ("La resta es: ", resta)
print ("La multiplicacion es: ", multi)
print ("La división es: ", div)
print ("La división entera es: ", DivEnt)


